import java.util.Map;

/**
 * Evaluates Current HEI Weight Data Definition
 */
@Handler(supports=HEICWCWeightCategoriesDataDefinition.class, order=50)
public class HEICWCWeightCategoriesDataEvaluator implements EncounterDataEvaluator {

    @Autowired
    private EvaluationService evaluationService;

    public EvaluatedEncounterData evaluate(EncounterDataDefinition definition, EvaluationContext context) throws EvaluationException {
        EvaluatedEncounterData c = new EvaluatedEncounterData(definition, context);

        String qry = "select encounter_id,\n" +
                "  (case weight_category when 123814 then \"Underweight(UW)\"\n" +
                "                        when 126598 then \"Severely Underweight(SUW)\"\n" +
                "                        when 114413 then \"Overweight(OW)\"\n" +
                "                        when 115115 then \"Obese(O)\" when 1115 then \"Normal(N)\" else \"\" end) as weight_category\n" +
                "from kenyaemr_etl.etl_hei_follow_up_visit;";

        SqlQueryBuilder queryBuilder = new SqlQueryBuilder();
        queryBuilder.append(qry);
        Map<Integer, Object> data = evaluationService.evaluateToMap(queryBuilder, Integer.class, Object.class, context);
        c.setData(data);
        return c;
    }
}
